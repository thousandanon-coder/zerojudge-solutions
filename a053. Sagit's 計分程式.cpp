#include<bits/stdc++.h> // 引入標準函式庫
using namespace std;

int main(){
	int a; // 定義變數 a，用來儲存答對的題數
	
	// 使用 while 搭配 cin 讀取輸入，直到沒有資料為止 (處理多筆輸入)
	while(cin >> a){
		
		// 第一階段：答對 0 到 10 題，每題 6 分
		if(a <= 10){
			cout << a * 6 << "\n";
		}
		
		// 第二階段：答對 11 到 20 題
		// 前 10 題共 60 分，第 11 題起每題 2 分
		if(a >= 11 && a <= 20){
			cout << (a - 10) * 2 + 60 << "\n";
		}
		
		// 第三階段：答對 21 到 40 題
		// 前 20 題共 80 分 (60 + 20)，第 21 題起每題 1 分
		if(a >= 21 && a <= 40){
			cout << (a - 20) + 80 << "\n";
		}
		
		// 第四階段：答對超過 40 題，一律以 100 分計
		if(a >= 41){
			cout << "100" << "\n";
		}
	}
	
	return 0; // 程式正常結束
}
