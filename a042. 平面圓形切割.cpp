/*
題目: a042. 平面圓形切割
難度: 入門
完成日期: 2026-02-24 21:26
公式說明：
n 個圓最多可將平面分割成 f(n) 個區域。
觀察規律：
f(1) = 2
f(2) = 4
f(3) = 8
由此推導出遞迴關係：f(n) = f(n-1) + 2(n-1)
最終公式為：n^2 - n + 2
*/

#include <bits/stdc++.h>
using namespace std;

int main() {
	// 宣告變數 n 代表圓的數量
	int n;
	
	// 使用 while 迴圈持續讀取輸入，直到沒有資料為止 (EOF)
	while(cin >> n) {
		/* 輸出切割後的區域數量。
		公式 n*n - n + 2 的由來：
		每個新加入的第 n 個圓，最多會與前 (n-1) 個圓各交於 2 個點，
		這些交點會將新圓周切成 2(n-1) 段弧，每一段弧都會將原有的一個區域一分為二。
		*/
		cout << n * n - n + 2 << "\n";
	}
	
	return 0;    
}
