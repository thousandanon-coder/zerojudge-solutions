/*
題目: a044. 空間切割
難度: 入門
完成日期: 2026-01-30 15:37
說明：本題要求 n 個平面最多能將三維空間切割成多少個區域。
公式推導：
1. 0 維（點）切線：1 (1)
2. 1 維（線）切面：(n^2 + n + 2) / 2
3. 2 維（平面）切空間：(n^3 + 5n + 6) / 6
*/
#include<bits/stdc++.h>
using namespace std;

int main() {
	int n;
	// 使用 while 迴圈持續讀取輸入，直到檔案結束 (EOF)
	while (cin >> n) {
		/* n 個平面切割空間的最大區域數公式為：
		L(n) = (n^3 + 5*n + 6) / 6
		
		這個公式也可以看作是組合數的累積：
		C(n, 0) + C(n, 1) + C(n, 2) + C(n, 3)
		代表在三維空間中，由 0 維、1 維、2 維到 3 維特徵所構成的分割總數。
		*/
		cout << (n * n * n + 5 * n + 6) / 6 << "\n";
	}
	return 0;
}
